namespace MagePrakash\ProductAttachment\Controller\Adminhtml\Attachment;

class MassDelete extends \Magento\Backend\App\Action {

    protected $_filter;

    protected $_collectionFactory;

    public function __construct(
        \Magento\Ui\Component\MassAction\Filter $filter,
        \MagePrakash\ProductAttachment\Model\ResourceModel\Attachment\CollectionFactory $collectionFactory,
        \Magento\Backend\App\Action\Context $context
        ) {
        $this->_filter            = $filter;
        $this->_collectionFactory = $collectionFactory;
        parent::__construct($context);
    }

    public function execute() {
        try{ 

            $attachmentCollection = $this->_filter->getCollection($this->_collectionFactory->create());
            foreach ($attachmentCollection as $item) {
                $item->delete();
            }
            $this->messageManager->addSuccess(__('Attachments Deleted Successfully.'));
        }catch(Exception $e){
            $this->messageManager->addError($e->getMessage());
        }
        $resultRedirect = $this->resultRedirectFactory->create();
        return $resultRedirect->setPath('*/*/');
    }
}

